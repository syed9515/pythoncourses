"""
type casting is use to convert int==>float,int==>str,float==>str,float==>int,str==>int and str==>float.
"""

       #int==>float

print('int==>float')

x=100
y=float(x)
print(type(y),y)

       #int==>str

print('int==>str')

x=150
y=str(x)
print(type(y),y)

       #float==>str

print('float==>str')

x=200.0
y=str(x)
print(type(y),y)

       #float==>int

print('float==>int')

x=250.50
y=int(x)
print(type(y),y)

       #str==>int

print('str==>int')

x='300'
